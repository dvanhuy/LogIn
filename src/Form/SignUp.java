    private void AddtoListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddtoListActionPerformed
        User user = new User();
        user.setTen(ten.getText());
        user.setSdt(sodienthoai.getText());
        user.setTenTK(taikhoan.getText());
        user.setMatKhau(String.valueOf(matkhau.getPassword()));
        user.setGioiThieu(gioithieu.getText());
        
        if (checknhac.isSelected() && checkphim.isSelected())
             user.setSoThich("Nghe nhạc,Xem phim");
        else if (checknhac.isSelected())
            user.setSoThich("Nghe nhạc");
        else if (checkphim.isSelected())
            user.setSoThich("Xem phim");
        else user.setSoThich("");
        
        String vaitro="User";
        if (jRadioAdmin.isSelected())
            vaitro="Admin";
        if (jRadioUser.isSelected())
            vaitro="User";            
        user.setVaiTro(vaitro);
        if (jCheckBoxsure.isSelected())
        {
            if (taikhoan.getText().equals("") || String.valueOf(matkhau.getPassword()).equals("") )
            {
                JOptionPane.showMessageDialog
                (this, "Chưa nhập tài khoản hoặc mật khẩu", "Cảnh báo", JOptionPane.INFORMATION_MESSAGE);
            }
            else 
            {
                userService.addUser(user);
                new SignIn(user).setVisible(true);
                this.dispose();
            }
        }
        else 
        {
            JOptionPane.showMessageDialog(this, "Bạn chưa xác nhận", "Hãy xác nhận", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_AddtoListActionPerformed

